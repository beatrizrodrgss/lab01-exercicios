# Escreva um programa que leia o nome de um produto a partir do teclado. 
# Em seguida, imprima a string lida convertendo todas as letras para minúsculas.

# Leitura do nome do produto
nome_produto = input("Digite o nome do produto: ")

# Conversão para minúsculas
nome_produto_minusculo = nome_produto.lower()

# Saída
print("Nome do produto em minúsculas:", nome_produto_minusculo)
