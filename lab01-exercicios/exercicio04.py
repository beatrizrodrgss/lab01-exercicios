# Escreva um programa que leia o nome completo de uma pessoa. 
# Em seguida, imprima a string lida convertendo todas as letras para MAIÚSCULAS.

# Leitura do nome completo da pessoa
nome_completo = input("Digite o nome completo da pessoa: ")

# Conversão para maiúsculas
nome_completo_maiusculo = nome_completo.upper()

# Saída
print("Nome completo em maiúsculas:", nome_completo_maiusculo)
